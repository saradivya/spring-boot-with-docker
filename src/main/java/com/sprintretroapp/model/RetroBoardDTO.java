package com.sprintretroapp.model;


public class RetroBoardDTO {
  public String sprintBoardNumber;
  public String sprintContext;
  public String sprintTitle;

  public String getSprintBoardNumber() {
    return sprintBoardNumber;
  }

  public void setSprintBoardNumber(String sprintBoardNumber) {
    this.sprintBoardNumber = sprintBoardNumber;
  }

  public String getSprintContext() {
    return sprintContext;
  }

  public void setSprintContext(String sprintContext) {
    this.sprintContext = sprintContext;
  }

  public String getSprintTitle() {
    return sprintTitle;
  }

  public void setSprintTitle(String sprintTitle) {
    this.sprintTitle = sprintTitle;
  }
}
